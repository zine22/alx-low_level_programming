/**                                                                                                                                                  
* _isupper - function to check if c is uppercase                                                                                                     
*                                                                                                                                                    
* @c: number to be checked                                                                                                                           
* Return: 1 if c is uppercase and 0 if not                                                                                                           
*/

int _isupper(int c)
{
for (c = 65; c < 90; c++)
return (1);
return (0);
}
